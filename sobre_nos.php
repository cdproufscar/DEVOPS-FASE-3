  <section class="historia" id="spc">
    <h1 class="titulo_sobre">História</h1>
    <p class="t-his">
        O projeto “AssistiVerse - Desenvolvimento de Plataforma Digital para Personalização e Auxílio à Aquisição e Montagem de Meios Auxiliares de Locomoção”, é uma iniciativa da Finep que busca facilitar a vida de crianças com mobilidade reduzida, em especial, criança de até 8 anos. A ideia é criar uma plataforma digital que ajude na escolha e montagem de dispositivos que auxiliam na locomoção.
    </p> <br>

    <p class="t-his">

        Esses dispositivos são super importantes para ajudar pessoas com deficiências a se moverem e se desenvolverem de forma mais completa, tanto fisicamente quanto cognitiva e socialmente. No Brasil, é difícil encontrar opções baratas e adequadas para pessoas com necessidades mais específicas, pois cada usuário requer tecnologias assistivas que sejam capazes de ajustes e personalizações, bem como sejam acessíveis em relação ao custo, mantendo a qualidade. 
    </p><br>

    <p class="t-his">
        Como forma de resolução, utilizamos tecnologias como impressão 3D e corte a laser para criar equipamentos acessíveis e personalizados. Esses protótipos passam por testes com fisioterapeutas e terapeutas ocupacionais para garantir que funcionem bem e alcancem as expectativas dos usuários

    </p>


  </section>

